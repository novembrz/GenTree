import SwiftUI

extension Color {
    
    static func accent() -> Color {
        return Color("AccentColor")
    }
    
    static func background() -> Color {
        return Color("BgColor")
    }
    
    static func element() -> Color {
        return Color("ElementColor")
    }
    
    static func greenColor() -> Color {
        return Color("GreenColor")
    }

    static func lightGreenColor() -> Color {
        return Color("LightGreenColor")
    }
    
    static func text() -> Color {
        return Color("TextColor")
    }
    
    static func secondText() -> Color {
        return Color("SecondTextColor")
    }
    
    static func line() -> Color {
        return Color("LineColor")
    }
    
    static func dangerous() -> Color {
        return Color("DangerouseColor")
    }
}
